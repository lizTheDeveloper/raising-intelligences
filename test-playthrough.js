#!/usr/bin/env node
/**
 * Automated solo-game walkthrough using Playwright.
 *
 * Prerequisites:
 *   npm install -g playwright && npx playwright install chromium
 *
 * Usage:
 *   node test-playthrough.js [BASE_URL]
 *   HEADLESS=0 node test-playthrough.js   # show the browser window
 *
 * The script drives the full arc:
 *   start → guardian screen → event intro → family chat →
 *   debrief → epilogue → report card
 */

// @ts-check
const { chromium } = require("playwright");

const BASE_URL = process.argv[2] || "http://localhost:5173";
const CHILD_NAME = "Luna";
// LLM calls (event generation, psychologist, epilogue, report card) can take
// 45–90 seconds via OpenRouter. Give each step generous headroom.
const LLM_TIMEOUT_MS = 120_000;
const HEADLESS = process.env.HEADLESS !== "0";

async function run() {
  const browser = await chromium.launch({ headless: HEADLESS });
  const page = await browser.newPage();

  try {
    console.log(`[playtest] → ${BASE_URL}`);
    await page.goto(BASE_URL, { waitUntil: "networkidle", timeout: 20_000 });

    // ── Start screen ─────────────────────────────────────────────────────────
    // Fill in the child's name and click "begin" to create the game.
    console.log("[playtest] start: entering child name");
    await page.fill("input.name-input", CHILD_NAME);
    await page.click('[data-testid="btn-begin"]');

    // ── Guardian screen ───────────────────────────────────────────────────────
    // The guardian screen plays a short intro animation while the first event
    // is generated by the World Manager LLM. The "I'm ready" button only
    // appears once *both* the animation has completed and the event has loaded.
    // Use [data-testid="btn-guardian-ready"] — not button text — to avoid
    // brittleness when copy changes.
    console.log("[playtest] guardian: waiting for ready button (LLM generation)");
    await page.waitForSelector('[data-testid="btn-guardian-ready"]', {
      timeout: LLM_TIMEOUT_MS,
    });
    await page.click('[data-testid="btn-guardian-ready"]');

    // ── Event intro ───────────────────────────────────────────────────────────
    console.log("[playtest] event intro: entering the scene");
    await page.waitForSelector('[data-testid="btn-enter-event"]', {
      timeout: 10_000,
    });
    await page.click('[data-testid="btn-enter-event"]');

    // ── Family chat ───────────────────────────────────────────────────────────
    const chatLines = [
      "Hi sweetheart, what happened today?",
      "That's okay, we're here for you.",
    ];
    for (const line of chatLines) {
      console.log(`[playtest] chat: "${line.slice(0, 40)}"`);
      // Wait until the input is enabled (previous streaming response finished).
      await page.waitForSelector("form.message-input input:not([disabled])", {
        timeout: LLM_TIMEOUT_MS,
      });
      await page.fill("form.message-input input", line);
      await page.click('form.message-input button[type="submit"]');
      // The "end conversation" button re-enables once streaming is done.
      await page.waitForSelector('[data-testid="btn-end-chat"]:not([disabled])', {
        timeout: LLM_TIMEOUT_MS,
      });
    }

    console.log("[playtest] chat: ending conversation");
    await page.click('[data-testid="btn-end-chat"]');

    // ── Debrief ───────────────────────────────────────────────────────────────
    // After end-chat the Psychologist LLM runs, then the debrief screen appears.
    // Skip to the epilogue instead of looping to a second event.
    console.log("[playtest] debrief: requesting epilogue");
    await page.waitForSelector('[data-testid="btn-epilogue"]', {
      timeout: LLM_TIMEOUT_MS,
    });
    await page.click('[data-testid="btn-epilogue"]');

    // ── Epilogue ──────────────────────────────────────────────────────────────
    console.log("[playtest] epilogue: waiting for continue button");
    await page.waitForSelector('[data-testid="btn-continue"]', {
      timeout: LLM_TIMEOUT_MS,
    });
    await page.click('[data-testid="btn-continue"]');

    // ── Report card ───────────────────────────────────────────────────────────
    console.log("[playtest] report card: waiting for completion");
    await page.waitForSelector(".report-card", { timeout: LLM_TIMEOUT_MS });

    const heading = await page
      .locator(".report-card h1, .report-card h2")
      .first()
      .textContent({ timeout: 5_000 })
      .catch(() => null);

    console.log(`[playtest] PASS — report card visible (heading: "${heading}")`);
  } finally {
    await browser.close();
  }
}

run().catch((err) => {
  console.error("[playtest] FAIL:", err.message);
  process.exit(1);
});
